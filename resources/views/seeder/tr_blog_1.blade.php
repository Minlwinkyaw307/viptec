<p><strong>Maket Bıçağı Kullanılırken Dikkat Edilmesi Gerekenler</strong></p>

<p>1) Bıçak ucunuzun keskin olduğundan emin olun. Keskin bıçak ucu paslanmış ya da deforme olmuş bıçak ucuna göre çok
    daha güvenli olacaktır.</p>

<p>2) Kesim yaparken kesiğe dirençli eldiven kullanın. Kesiğe dirençli eldiven herhangi bir kaza durumunda ellerinizi
    kesikten koruyacaktır</p>

<p>3) Riskli kesimler için güvenlikli maket bıçağı modellerini kullanın. Güvenlikli maket bıçağı parmağınızı bıçak
    sürgüsünden çektiğinizde bıçak ucunun otomatik olarak&nbsp; bıçak gövdesi içerisine tekrar sokacaktır.</p>

<p>4) Maket bıçağını kullanmadan önce bıçak ucunun doğru yerleştirildiğine emin olun.</p>

<p>5) Ellerinizi ve kollarınızı kesim alanından olabildiğince uzak tutun.</p>

<p>6) Maket bıçağına gereğinden fazla kuvvet uygulamayın</p>

<p>7) Bıçak ucunu değiştirirken üretici firmanın talimatlarına uyun.</p>

<p>8) Körelmiş ve kırılmış bıçak uçlarını delinmeye dayanıklı kutularda muhafaza edin.</p>

<p>9) Kesim sırasında koruyucu gözlük kullanın. Kesim sırasında bıçak ucu istenmeyen şekilde kırılır ve etrafa sıçrarsa
    gözlük gözlerinizi olası kazalardan koruyacaktır.</p>


