    blade edge requires much more force to be applied and it will make you exhausted. Applying too much force may also
    lead to some safety problems such as slippage of utility knife or damage on cutting material. For this reason, it is
    better to check the condition of the utility knife blade regularly and snap off its edge or replace it with the new
